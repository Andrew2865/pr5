#include <iostream>
#include <cmath>
using namespace std;
int main()
{
	double S, p, q, S(p, q), f(n), n;
		cout << "p" << cin >> p;
	cout << "q" << cin >> q;
	
	{
		cout << "n =" << cin >> n;
		if (n % 10 > 0)
			f(n) = n % 10;
		if (n = 0)
			f(n) = 0;
		else
			f(n) = f(n / 10);
	}

	{
		S = 0;
		S(p, q) = S;
		if (i = p, i < q, i++)
			S += f(n);
		else
			cout << "S(p, q)" << cin >> S(p, q);

	}
}
return 0;